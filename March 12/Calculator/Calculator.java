package Calculator;

public class Calculator {
	// Function to Add 2 numbers
	public int sum(int a,int b) {
		return (a+b);
	}
	
	// Function to Multiply 2 Numbers
	public int multiply(int a,int b) {
		return (a*b);
	}
	
	// Function to Divide 2 Numbers
	public int divide(int a,int b) {
		return (a/b);
	}
}
